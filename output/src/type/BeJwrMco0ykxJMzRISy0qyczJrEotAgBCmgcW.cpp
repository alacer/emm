TupleMappings* SELF::initMappings()
{
    instantiators_.insert(std::make_pair("tuple<int32 id,float32 fertilizer>",&initer));
    TupleMappings * tm = new TupleMappings();
#define MY_OFFSETOF(member, base) \
    ((uintptr_t)&reinterpret_cast<Self*>(base)->member) - (uintptr_t)base
   
    // initialize the mappings 
    
    {
        std::string s("id");
        TypeOffset t(MY_OFFSETOF(id_, tm), 
                     Meta::Type::typeOf<SPL::int32 >(), 
                     &typeid(SPL::int32));
        addMapping(*tm, t, s, 0);
    }
    
    {
        std::string s("fertilizer");
        TypeOffset t(MY_OFFSETOF(fertilizer_, tm), 
                     Meta::Type::typeOf<SPL::float32 >(), 
                     &typeid(SPL::float32));
        addMapping(*tm, t, s, 1);
    }
    
    return tm;
}

void SELF::deserialize(std::istream & istr, bool withSuffix)
{
   std::string s;
   char c;

   istr >> c; if (!istr) { return; }
   if (c != '{') { istr.setstate(std::ios_base::failbit); return; }
   
   if (!readAttributeIdentifier(istr, s)) { return; }
   if (s != "id") { istr.setstate(std::ios_base::failbit); return; }
   istr >> c; if (!istr) { return; }
   if (c != '=') { istr.setstate(std::ios_base::failbit); return; }
   if (withSuffix)
     SPL::deserializeWithSuffix(istr, id_);
   else
     istr >> id_;
   if (!istr) { return; }  
   istr >> c; if (!istr) { return; }
   if (c != ',') { istr.setstate(std::ios_base::failbit); return; }

   if (!readAttributeIdentifier(istr, s)) { return; }
   if (s != "fertilizer") { istr.setstate(std::ios_base::failbit); return; }
   istr >> c; if (!istr) { return; }
   if (c != '=') { istr.setstate(std::ios_base::failbit); return; }
   if (withSuffix)
     SPL::deserializeWithSuffix(istr, fertilizer_);
   else
     istr >> fertilizer_;
   if (!istr) { return; }  
   istr >> c; if (!istr) { return; }
   
   if (c != '}') { istr.setstate(std::ios_base::failbit); return; }
}

